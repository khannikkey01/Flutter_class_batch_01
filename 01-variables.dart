void main() {
  // lowerCamelCase for Variable names
  // type variableName = value ;

// String means collection of character or words
// whenever we want to store collection of character or words
// we use String
  String myName = "Nadeem Khan Qureshi";
  print(myName);

// int - interger
// int used for store numerical values
// whenever we want to store numerical values
// we use Int

  int age = 28;
  print(age);

// double - numbers with point (decimal numbers)
// whenever we want to store decimal numbers
// we use double

  double percentage = 84.22;
  print(percentage);

// bool - Boolean (True or Flase)
// it will always return (True-False or 0-1)

  bool male = true;
  print(male);
}
